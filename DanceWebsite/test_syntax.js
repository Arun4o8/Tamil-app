            // ===================== COLORING GAME LOGIC =====================
            let isDrawing = false;
            let lastCoordinate = { x: 0, y: 0 };
            const coloringCanvas = document.getElementById('coloring-canvas');
            const ctx = coloringCanvas ? coloringCanvas.getContext('2d') : null;
            let brushColor = '#e91e63';
            let brushSize = 6;

            function getPos(e, canvas) {
                const rect = canvas.getBoundingClientRect();
                if (!rect.width || !rect.height) return { x: 0, y: 0 };
                const scaleX = canvas.width / rect.width;
                const scaleY = canvas.height / rect.height;
                const clientX = e.clientX || (e.touches && e.touches[0] ? e.touches[0].clientX : 0);
                const clientY = e.clientY || (e.touches && e.touches[0] ? e.touches[0].clientY : 0);
                return {
                    x: (clientX - rect.left) * scaleX,
                    y: (clientY - rect.top) * scaleY
                };
            }

            function startDraw(e) {
                const canvas = document.getElementById('coloring-canvas');
                if (!canvas) return;
                isDrawing = true;
                lastCoordinate = getPos(e, canvas);
            }

            function draw(e) {
                if (!isDrawing) return;
                const canvas = document.getElementById('coloring-canvas');
                if (!canvas) return;
                const ctx = canvas.getContext('2d');
                const pos = getPos(e, canvas);
                ctx.beginPath();
                ctx.moveTo(lastCoordinate.x, lastCoordinate.y);
                ctx.lineTo(pos.x, pos.y);
                ctx.strokeStyle = brushColor;
                const sizeInput = document.getElementById('brush-size');
                ctx.lineWidth = sizeInput ? sizeInput.value : brushSize;
                ctx.lineCap = 'round';
                ctx.lineJoin = 'round';
                ctx.stroke();
                lastCoordinate = pos;
            }

