        // THE MAIN TRANSLATION FUNCTION
        window.changeLanguage = function (lang) {
            console.log('?? changeLanguage called with: ' + lang);
            localStorage.setItem('selectedLanguage', lang);

            var t = (lang === 'ta') ? window.TAMIL : window.ENGLISH;

            // 1. Translate all data-i18n elements
            var i18nEls = document.querySelectorAll('[data-i18n]');
            console.log('Found ' + i18nEls.length + ' data-i18n elements');
            i18nEls.forEach(function (el) {
                var key = el.getAttribute('data-i18n');
                if (t[key] !== undefined) {
                    el.innerText = t[key];
                }
            });

            // 2. Specific IDs (belt-and-suspenders approach)
            var idMap = {
                'welcome-text': 'welcome_to',
                'app-title': 'our_culture',
                'home-dances-label': 'home_dances',
                'home-dances-desc': 'home_dances_desc',
                'home-celebrations-label': 'home_celebrations',
                'home-celebrations-desc': 'home_celebrations_desc',
                'discovery-hub-title': 'discovery_hub',
                'edit-profile-btn': 'edit_profile',
                'dark-mode-label': 'dark_mode',
                'quiz-title-main': 'cultural_quiz',
                'quiz-intro-h4': 'master_heritage',
                'quiz-intro-p': 'test_knowledge',
                'start-quiz-btn': 'start_challenge',
                'quiz-complete-h4': 'quiz_complete',
                'try-again-btn': 'try_again',
                'saved-item-text': 'no_saved_items',
            };
            Object.keys(idMap).forEach(function (id) {
                var el = document.getElementById(id);
                if (el && t[idMap[id]]) el.innerText = t[idMap[id]];
            });

            // 3. Bottom nav labels
            var navElements = document.querySelectorAll('.nav-item');
            navElements.forEach(function (navItem) {
                var id = navItem.id.replace('nav-', '');
                var label = navItem.querySelector('.nav-label');
                if (label && t[id]) label.innerText = t[id];
            });

            // 4. Discovery Hub buttons
            var discLabels = document.querySelectorAll('.discovery-label');
            if (discLabels[0] && t['challenge']) discLabels[0].innerText = t['challenge'];
            if (discLabels[1] && t['cultural_quiz']) discLabels[1].innerText = t['cultural_quiz'];
            if (discLabels[2] && t['rules_guidelines']) discLabels[2].innerText = t['rules_guidelines'];

            // 5. Category tabs
            var catTabs = document.querySelectorAll('#view-explore .tab');
            var tabKeys = ['all_dances', 'classical', 'folk', 'martial_arts'];
            catTabs.forEach(function (tab, i) {
                if (tabKeys[i] && t[tabKeys[i]]) tab.innerText = t[tabKeys[i]];
            });

            // 6. Dance names (by class)
            var enT = window.ENGLISH;
            var taT = window.TAMIL;
            document.querySelectorAll('.dance-name').forEach(function (el) {
                var txt = el.innerText.trim();
                // Try to find the key by matching English or Tamil value
                var key = null;
                Object.keys(enT).forEach(function (k) { if (enT[k] === txt) key = k; });
                Object.keys(taT).forEach(function (k) { if (taT[k] === txt) key = k; });
                if (key && t[key]) el.innerText = t[key];
            });

            // 7. Dance type subtitles
            var allDanceTypes = document.querySelectorAll('.dance-type');
            allDanceTypes.forEach(function (el) {
                var txt = el.innerText.trim();
                // Find matching English key
                var key = null;
                Object.keys(enT).forEach(function (k) { if (enT[k] === txt) key = k; });
                Object.keys(taT).forEach(function (k) { if (taT[k] === txt) key = k; });
                if (key && t[key]) el.innerText = t[key];
            });

            // 8. "View Details" links
            document.querySelectorAll('.view-details-label').forEach(function (el) {
                if (t['view_details']) el.innerText = t['view_details'];
            });

            // 9. Back to Home buttons (all of them)
            document.querySelectorAll('.back-home-btn').forEach(function (btn) {
                if (t['back_to_home']) btn.innerText = t['back_to_home'];
            });

            // 10. Start buttons
            document.querySelectorAll('.start-btn-label').forEach(function (btn) {
                if (t['start']) btn.innerHTML = '<i class="fas fa-play"></i> ' + t['start'];
            });

            // 11. Explore Now button
            var exploreNowBtn = document.querySelector('[data-i18n="explore_now"]');
            if (exploreNowBtn && t['explore_now']) exploreNowBtn.innerText = t['explore_now'];

            // 12. Language radio icons update
            var langChecks = document.querySelectorAll('#view-language .glass-panel i');
            if (langChecks.length >= 2) {
                if (lang === 'en') {
                    langChecks[0].className = 'fas fa-check-circle'; langChecks[0].style.color = 'var(--primary)';
                    langChecks[1].className = 'far fa-circle'; langChecks[1].style.color = 'var(--text-muted)';
                } else {
                    langChecks[0].className = 'far fa-circle'; langChecks[0].style.color = 'var(--text-muted)';
                    langChecks[1].className = 'fas fa-check-circle'; langChecks[1].style.color = 'var(--primary)';
                }
            }

            // 13. Toast
            var toast = document.getElementById('toast');
            if (toast) {
                toast.innerText = (lang === 'ta') ? 'மொழி மாற்றப்பட்டது!' : '? Language Changed!';
                toast.style.transform = 'translateX(-50%) translateY(0)';
                setTimeout(function () { toast.style.transform = 'translateX(-50%) translateY(150px)'; }, 2500);
            }

            console.log('? Translation complete: ' + lang.toUpperCase() + ' | Elements updated: ' + i18nEls.length);
        };


