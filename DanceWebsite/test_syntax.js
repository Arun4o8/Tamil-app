            // Override showCultureDetail to hide learning for celebrations
            const originalShowCultureDetail = window.showCultureDetail;
            window.showCultureDetail = function(name) {
                if (typeof originalShowCultureDetail === 'function') {
                    originalShowCultureDetail(name);
                }
                
                // Hide learning options if it's a celebration
                const learningHeading = document.querySelector('#view-culture-detail h4:nth-last-of-type(1)');
                const learningButtons = document.querySelector('#view-culture-detail div[style*="flex-direction: column"]');
                const startTrainingBtn = document.getElementById('detail-start-training-btn');
                
                if (celebrationNames.includes(name)) {
                    if (learningHeading) learningHeading.style.display = 'none';
                    if (learningButtons) learningButtons.style.display = 'none';
                    if (startTrainingBtn) startTrainingBtn.style.display = 'none';
                } else {
                    if (learningHeading) learningHeading.style.display = 'block';
                    if (learningButtons) learningButtons.style.display = 'flex';
                    if (startTrainingBtn) startTrainingBtn.style.display = 'flex';
                }
            };

            // Enhance switchView to handle nav highlights and saved rendering
            const baseSwitchView = window.switchView;
            window.switchView = function(viewName) {
                if (typeof baseSwitchView === 'function') baseSwitchView(viewName);
                
                // Active Nav State
                document.querySelectorAll('.nav-item').forEach(el => el.classList.remove('active'));
                const navId = 'nav-' + viewName;
                if (document.getElementById(navId)) {
                    document.getElementById(navId).classList.add('active');
                }
                
                if (viewName === 'saved') renderSavedItems();
                
                // Hide home header button if on home view
                const homeBtn = document.querySelector('.app-header button[onclick*="index.html"]');
                if (homeBtn) {
                    homeBtn.style.display = (viewName === 'home') ? 'none' : 'flex';
                }
            };

